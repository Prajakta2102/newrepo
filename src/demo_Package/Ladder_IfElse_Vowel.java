package demo_Package;
public class Ladder_IfElse_Vowel 
{
	public static void main(String[]args)
	{
		char ch='E';
		String str =" is vowel";
		if(ch=='A'||ch=='a')
		{
			System.out.println(ch + str);
		}
		else if(ch=='E'||ch=='e')
		{
			System.out.println( ch +str);
		}
		else if(ch=='I'||ch=='i')
		{
			System.out.println(ch +str);
		}
		else if(ch=='O'||ch=='o')
		{
			System.out.println(ch +str);
		}
		else if(ch=='U'||ch=='u')
		{
			System.out.println(ch +str);
		}
		else
		{
			System.out.println(ch +" is Consonant");
		}
	}

}
